# Backend Package
# ===============
# This package contains the core backend components for the Qwen3 TTS system.
#
# Modules:
# - config.py: Configuration settings and environment variables
# - tts_engine.py: Core TTS engine wrapper for Qwen3 models
# - voice_cloner.py: Custom voice creation from audio samples
# - app.py: FastAPI application with REST API endpoints
# - models.py: Pydantic data models for request/response validation